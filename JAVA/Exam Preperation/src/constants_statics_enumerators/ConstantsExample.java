package constants_statics_enumerators;


// Constants are variables whose values cannot be changed once they are assigned. 
// In Java, you can use the final keyword to create constants.
public class ConstantsExample {
    public static final double PI = 3.14159;
    public static final int MAX_ATTEMPTS = 5;
    
    public static void main(String[] args) {
        System.out.println("PI value: " + PI);
        System.out.println("Maximum attempts: " + MAX_ATTEMPTS);
    }
}
